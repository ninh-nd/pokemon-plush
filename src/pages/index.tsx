import { Box, Container, Grid } from "@mui/material";
import { GetServerSideProps } from "next";
import Head from "next/head";
import { PokemonObject } from "~/api";
import PokemonCard from "~/components/Card";
import { getFirstPokemonPlush } from "../../prisma/pokemonPlush";
export const getServerSideProps: GetServerSideProps = async () => {
  const list = await getFirstPokemonPlush();
  return {
    props: {
      list,
    },
  };
};
type Props = {
  list: PokemonObject[];
};
export default function Home({ list }: Props) {
  return (
    <>
      <Head>
        <title>Pokemon Plush</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <Container
          maxWidth="lg"
          sx={{
            height: "100vh",
            display: "flex",
            justifyContent: "center",
            alignItems: "center",
          }}
        >
          <Box
            sx={{
              display: "flex",
              justifyContent: "center",
              flexDirection: "column",
              m: 2,
            }}
          >
            <Grid container spacing={2}>
              <Grid item xs={12}>
                <PokemonCard list={list} />
              </Grid>
            </Grid>
          </Box>
        </Container>
      </main>
    </>
  );
}
